lista_num =[2,3,7,12,16,27]
listaPar = []
listaImpar = []
def num_parImpar():
    for i in lista_num:
        if i % 2 == 0:
            listaPar.append
        else:
            listaImpar.append
            
print("Numeros Pares:", listaPar)
print("Numeros Impares:", listaImpar)

num_parImpar()